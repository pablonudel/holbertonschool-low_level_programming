#include "3-calc.h"
/**
 * op_add - returns the sum of a and b.
 * @a: int - first number
 * @b: int - second number
 *
 * Return: int - result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the diff of a and b.
 * @a: int - first number
 * @b: int - second number
 *
 * Return: int - result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the prod of a and b.
 * @a: int - first number
 * @b: int - second number
 *
 * Return: int - result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the div of a and b.
 * @a: int - first number
 * @b: int - second number
 *
 * Return: int - result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the mod of a and b.
 * @a: int - first number
 * @b: int - second number
 *
 * Return: int - result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
